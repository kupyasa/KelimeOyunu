    private void düzenleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_düzenleButtonActionPerformed
        // TODO add your handling code here:
        if (kelimeninTanımıTextArea.getText().isEmpty() || kelimeTextField.getText().trim().length() < 4 || kelimeTextField.getText().trim().length() > 10) {
            JOptionPane.showMessageDialog(null, "Lütfen alanları düzgün bir şekilde doldurunuz.");
        } else {
            int seçilenIndex = kelimeSeçComboBox.getSelectedIndex();

            sorular.get(seçilenIndex).tanım = kelimeninTanımıTextArea.getText();
            sorular.get(seçilenIndex).kelime = kelimeTextField.getText().trim().toUpperCase();

            soruyuDosyayaKaydet();
            
            kelimelerDizi = new String[sorular.size()];
            
            for (int i = 0; i < sorular.size(); i++) {
                kelimelerDizi[i] = sorular.get(i).kelime;
            }
            kelimeSeçComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(kelimelerDizi));

            kelimeSeçComboBox.setSelectedIndex(0);

            JOptionPane.showMessageDialog(null, "Kelime tanımı ve kelime başarıyla düzenlendi.");

        }

    }//GEN-LAST:event_düzenleButtonActionPerformed

    private void silButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silButtonActionPerformed
        // TODO add your handling code here:
        int seçilenIndex = kelimeSeçComboBox.getSelectedIndex();

        sorular.remove(seçilenIndex);

        soruyuDosyayaKaydet();
        
        kelimelerDizi = new String[sorular.size()];

        for (int i = 0; i < sorular.size(); i++) {
            kelimelerDizi[i] = sorular.get(i).kelime;
        }
        kelimeSeçComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(kelimelerDizi));

        kelimeSeçComboBox.setSelectedIndex(0);

        JOptionPane.showMessageDialog(null, "Kelime tanımı ve kelime başarıyla silindi.");
    }//GEN-LAST:event_silButtonActionPerformed

