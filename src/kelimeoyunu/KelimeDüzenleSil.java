    public void arrayListiDoldur() {
        try {
            FileInputStream file = new FileInputStream("Sorular.txt");
            ObjectInput inputFile = new ObjectInputStream(file);

            boolean dosyaSonu = false;
            while (!dosyaSonu) {
                try {
                    sorular.add((Soru) inputFile.readObject());
                } catch (EOFException e) {
                    dosyaSonu = true;
                } catch (Exception f) {
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            inputFile.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    public void soruyuDosyayaKaydet() {
        try {
            FileOutputStream file = new FileOutputStream("Sorular.txt");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);

            for (int i = 0; i < sorular.size(); i++) {
                outputFile.writeObject(sorular.get(i));
            }
            outputFile.close();

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }

